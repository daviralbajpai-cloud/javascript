// IIFE means Immediately Invoked Function Expression
// It is a function that runs as soon as it is defined

(function() {
    console.log("This is an IIFE");
})(); // if we dont put semicolon after the function it will give an error when we try to rumn another function after this IIFE because it will consider the next function as a part of this IIFE and it will give an error

// The above code will run immediately and print "This is an IIFE" to the console



